package pkg;

public class DataTypes {

	public static void main(String[] args) {
		
		byte a=10;
		System.out.println("Short value is "+a);
		
		short b=20;
		System.out.println("Short value is "+b);
		
		int c=30;
		System.out.println("Integer value is "+c);
		
		long d=2574579252559l;
		System.out.println("Long value is "+d);
		
		float df=25.34f;
		System.out.println("float value is "+df);
		
		double e=406;
		System.out.println("Double value is "+e);
		
		boolean f=30>20;
		System.out.println(f);
		
		char ch='H';
		System.out.println("Character value is "+ch);
		
		String str="Ancy";
		System.out.println("Name is "+str);

	}

}
